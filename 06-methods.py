list = []

# Método append: agrega al final
list.append("Xiamoi")
list.append("Motorola")
list.append("Blackberry")

print(list)

# Insert: Agrega un elemento en la posición especificada
list.insert(1, "Huawei")
print(list)

# Extend: Agrega otra colección al final de la lista actual
list2 = ["Samsung","Apple"]
list.extend(list2)
print(list)